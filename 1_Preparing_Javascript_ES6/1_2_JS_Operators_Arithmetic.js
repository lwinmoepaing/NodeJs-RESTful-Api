/**
|--------------------------------------------------
| JavaScript Operators & Arithmetic
|--------------------------------------------------
*/

let x = 10;
let y = 22;

// Add
console.log(x + y);

// Subtraction
console.log(x - y);

// Multiplication
console.log(x * y);

// Division
console.log(x / y);

// Modulus
console.log(x % y);

// Pre Increament
console.log(++x);

// Post Increment
x++;
console.log(x);

// Example Arithmetic
x = (100 + 50) * y;
console.log(x);
